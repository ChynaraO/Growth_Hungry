package DesignPatterns.BuilderExample;

public class FastFoodRestaurant {
    int burger;
    int fries;
    int coke;
    int toy;

    public int getBurger() {
        return burger;
    }

    public void setBurger(int burger) {
        this.burger = burger;
    }

    public int getFries() {
        return fries;
    }

    public void setFries(int fries) {
        this.fries = fries;
    }

    public int getCoke() {
        return coke;
    }

    public void setCoke(int coke) {
        this.coke = coke;
    }

    public int getToy() {
        return toy;
    }

    public void setToy(int toy) {
        this.toy = toy;
    }
}
